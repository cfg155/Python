age1 = 10
age2 = 12
# Sum
print(age1+age2)

# Minus
print(age1-age2)

# Multiply
print(age1*age2)

# Divide
print(age1/age2)

# Modulus
print(age1 % age2)
print(age2 % age1)

# Combine Strings
firstName = "Louis"
lastName = "Leonardo"
print(firstName + " " + lastName)

# Multiplied Strings
print("HI"*5)

# Get String Index
name = "Louis Leonardo"
print(name[0])  # get index 0
print(name[0:5])  # From index 0 to 4
print(name[:3])  # from index 0 to 2
print(name[:-2])  # get all indexes exept the last two characters
